package SSD.Lab7;

public class Person {

    private int height;

    private int age;

    private boolean isMale;

    public int getHeight() {
        return height;
    }

    public void setHeight(int height){
        this.height=height;
    }


    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    public boolean isMale() {
        return isMale;

    }


}

